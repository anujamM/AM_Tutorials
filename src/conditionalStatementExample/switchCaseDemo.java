package conditionalStatementExample;

public class switchCaseDemo {
	
	public static void main(String[] args) 
	{
		int a = 5;
		
		switch(a)
		{
		case 1:
			System.out.println("Good!");
		break;	
		
		case 2:
		System.out.println("Better!");
		break;
		
		case 3:
			System.out.println("Best!");
			break;
		
		default:
			System.out.println("Default");
		}
		

}
}